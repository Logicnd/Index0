const fs = require('fs');
const path = require('path');

const isVercel = Boolean(process.env.VERCEL);
const apiBase =
  process.env.API_BASE_URL !== undefined
    ? process.env.API_BASE_URL
    : isVercel
      ? ''
      : 'http://localhost:3000';
const backendUrl =
  process.env.BACKEND_URL || 'https://index0-backend.onrender.com';

const outPath = path.join(__dirname, '..', 'js', 'config.js');
const content = `// Generated by scripts/generate-client-config.js — edit .env / Vercel env vars instead
window.INDEX0_CONFIG = ${JSON.stringify({ apiBase, backendUrl }, null, 2)};

(function resolveApiBase() {
  const cfg = window.INDEX0_CONFIG || {};
  const configured =
    typeof cfg.apiBase === 'string' ? cfg.apiBase.trim() : null;
  const isLocal =
    window.location.hostname === 'localhost' ||
    window.location.hostname === '127.0.0.1';

  if (configured) {
    window.INDEX0_API_BASE = configured.replace(/\\/$/, '');
  } else if (isLocal) {
    window.INDEX0_API_BASE = 'http://localhost:3000';
  } else {
    window.INDEX0_API_BASE = '';
  }
})();
`;

fs.writeFileSync(outPath, content, 'utf8');
console.log(`Wrote ${outPath} (apiBase: ${apiBase || 'same-origin'})`);
